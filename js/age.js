let age;
let aged = [];

if (age < 2) {
    console.log("infant");
} else if (age < 4) {
    console.log("toddler");
} else if (age < 10) {
    console.log("child");
} else if (age < 13) {
    console.log("preteen");
} else if (age < 20) {
    console.log("teen");
} else {
    console.log("young adult");
}

aged.push(age);