const en = "Hello World";
const fr = "Bonjour le monde";
const sp = "Hola Mundo";
const de = "Hallo Welt";
let words = en;

if (words === fr) {
    console.log(fr);
}
else if (words === sp) {
    console.log(sp);
}
else if (words === de) {
    console.log(de);
}
else if (words === en) {
    console.log(en);
}