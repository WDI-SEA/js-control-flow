
const lang = 'e'

switch(lang) {
    case 'en':
        console.log("Hello World")
        break;
    case 'de':
        console.log("Hallo Welt")
        break;
    case 'es':
        console.log("Hola Mundo")
        break;
    case 'fr':
        console.log('Bonjour le monde')
        break;
    default:
        console.log("Unable to translate to your desiered language")
}