var grade = 23;

switch(grade) {
  case (grade >= 90):
    console.log("A");
    break;
  case (grade >= 80):
    console.log("B");
    break;
  case (grade >= 70):
    console.log("C");
    break;
  case (grade >= 60):
    console.log("D");
    break;
  default:
    console.log("F");
    break;
  }
