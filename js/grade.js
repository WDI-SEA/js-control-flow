var grade = 93;

switch (true) {
	case (grade >= 90):
	console.log('Good Job! You got an A!!');
	break;
	case (grade >= 80):
	console.log('Nice! You got a B!');
	break;
	case (grade >= 70):
	console.log('C');
	break;
	case (grade >= 60):
	console.log('D');
	break;
	default:
	console.log('You failed. better study.');
}